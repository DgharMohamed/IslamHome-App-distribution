  static int getSurahNumberByPage(int page) {
    int currentSurahNum = 1;
    for (var entry in surahStartPages.entries) {
      if (entry.value <= page) {
        currentSurahNum = entry.key;
      } else {
        break;
      }
    }
    return currentSurahNum;
  }

  static String getSurahNameByPage(int page) {
    int currentSurahNum = 1;
    for (var entry in surahStartPages.entries) {
      if (entry.value <= page) {
        currentSurahNum = entry.key;
      } else {
        break;
      }
    }
    return surahNames[currentSurahNum] ?? '';
  }

  static int getJuzByPage(int page) {
    if (page <= 21) return 1;
    if (page <= 604) return ((page - 2) / 20).floor() + 1;
    return 1;
  }

  static double getProgressPercentage(int page) {
    return (page / 604).clamp(0.0, 1.0);
  }

  static bool isArabic(String text) {
    return RegExp(r'[\u0600-\u06FF]').hasMatch(text);
  }
